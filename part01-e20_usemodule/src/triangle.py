"""
this calcs area and hypotenuse of triangle
"""
import math 
def area(a, b):
    """area of triangle via a*b/2"""
    return a*b/2

def hypotenuse(a, b):
    """calcing hypotenuse of rightangle triangle"""
    return math.sqrt(a**2 + b**2)

