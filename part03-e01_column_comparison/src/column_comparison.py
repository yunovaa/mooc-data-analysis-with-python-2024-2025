#!/usr/bin/env python3

import numpy as np

def column_comparison(a):
    c = a[:, 1]
    d = a[:, -2]
    return a[c>d]
    
def main():
    a = np.array([[ 0.44174704, 0.82700036],
                    [ 0.53712473, -0.65215443],
                    [ 1.03262464,  1.43402233],
                    [-0.28293237 , 0.18947669],
                    [ 1.19666483 ,-0.03708419],
                    [-1.05046597 , 1.22374714],
                    [ 1.33287982 , 0.3598181 ],
                    [ 0.39145744 , 1.39491572],
                    [-0.02907603 , 1.17237931]])
    print(column_comparison(a))

if __name__ == "__main__":
    main()
